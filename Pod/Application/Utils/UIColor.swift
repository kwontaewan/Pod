import UIKit

extension UIColor {
    
    public class var reddishOrange: UIColor {
        return UIColor(red: 233.0 / 255.0, green: 78.0 / 255.0, blue: 27.0 / 255.0, alpha: 1.0)
    }
    
    public class var lightBlack: UIColor {
        return UIColor(red: 34.0 / 255.0, green: 34.0 / 255.0, blue: 34.0 / 255.0, alpha: 1.0)
    }
    
}
